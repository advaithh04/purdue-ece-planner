function generateGradeDistribution(avgGPA: number): { A: number; B: number; C: number; D: number; F: number } {
  const base = avgGPA / 4;
  return {
    A: Math.round(base * 35 + Math.random() * 10),
    B: Math.round(30 + Math.random() * 10),
    C: Math.round((1 - base) * 25 + Math.random() * 10),
    D: Math.round((1 - base) * 15 + Math.random() * 5),
    F: Math.round((1 - base) * 8 + Math.random() * 3),
  };
}

